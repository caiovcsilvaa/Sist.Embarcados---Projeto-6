static void BuildSine(uint16_t amplitude)
{
    for (uint16_t i = 0; i < SINE_SAMPLES; i++)
    {
        float theta = (2.0f * M_PI * i) / SINE_SAMPLES;
        sine_buf[i] = (uint16_t)((amplitude / 2) * (sinf(theta) + 1.0f));
    }
}

