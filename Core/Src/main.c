static void SetLedFromPot(uint8_t pot)
{
	uint32_t arr = __HAL_TIM_GET_AUTORELOAD(&htim3);     // 0-999
	pwm_duty = (pot * arr) / 255;
	__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, pwm_duty);
}

